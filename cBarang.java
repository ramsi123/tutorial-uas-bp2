package tutorialFinalProjectUAS;

public class cBarang {
    String nama;
    int harga;
    
    //constructor
    cBarang(String n, int h) {
        nama = n;
        harga = h;
    }
    
    //getter
    public String getNama() {
        return nama;
    }
    public int getHarga() {
        return harga;
    }
}
